void main() {
// Declare a variable by using var keyword without type-checking
  var name = "Tarun";
  print(name);
  var age = 24;
  print(age);

// Dart supports type-checking by prefixing the variable name with the appropriate data type
  String title = "Jana";
  int weight = 70;
  double height = 5.11;
  print(title);
  print(weight);
  print(height);
}
